void PWM_Set_Duty(TIM_HandleTypeDef *htim, uint32_t Channel, uint8_t dutyPercent)
{
  if (dutyPercent > 100)
    dutyPercent = 100;

  uint32_t arr = __HAL_TIM_GET_AUTORELOAD(htim);

  // Use ARR+1 for correct full-scale mapping
  uint32_t ccr = ((uint32_t)dutyPercent * (arr + 1)) / 100;

  // Safety clamp
  if (ccr > arr)
    ccr = arr;

  __HAL_TIM_SET_COMPARE(htim, Channel, ccr);
}

