/**
 * @brief  EXTI line detection callback.
 * @param  GPIO_Pin: Specifies the pins connected to the EXTI line.
 */
void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
  /* 1. Verify the interrupt came from PB3 */
  if (GPIO_Pin == GPIO_PIN_3)
  {
    /* Define a variable to track if a high-priority task needs to run */
    BaseType_t xHigherPriorityTaskWoken = pdFALSE;

    /* 2. Notify the Camera Task
     * vTaskNotifyGiveFromISR is the interrupt-safe version of the notify function.
     * It increments the notification count of the target task.
     */
    if (xCameraTaskHandle != NULL)
    {
      vTaskNotifyGiveFromISR(xCameraTaskHandle, &xHigherPriorityTaskWoken);
    }

    /* 3. Force a Context Switch (Crucial!)
     * If xHigherPriorityTaskWoken was set to pdTRUE inside the notify function,
     * this macro forces the RTOS to switch to the Camera Task immediately
     * after this ISR finishes, rather than waiting for the next tick.
     */
    portYIELD_FROM_ISR(xHigherPriorityTaskWoken);
  }
}


