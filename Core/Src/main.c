void TestTaskPWM(void *arguments)
{
  uint8_t duty = 0;
  int8_t step = 1; // +1 going up, -1 going down

  while (1)
  {
    PWM_Set_Duty(&htim1, TIM_CHANNEL_1, duty);

    duty += step;

    if (duty >= 100)
    {
      duty = 100;
      step = -1; // start decreasing
    }
    else if (duty == 0)
    {
      step = 1; // start increasing
    }

    HAL_Delay(10); // Smaller delay = smoother fade
  }
}

void CameraTask(void *arguments)
{
  const char *captureCommand = "C\r\n";
  static TickType_t lastCaptureTime = 0;
  const TickType_t debounceDelay = pdMS_TO_TICKS(250);  // 150ms debounce
  
  while (1)
  {
    // Wait indefinitely for notification from ISR
    ulTaskNotifyTake(pdTRUE, portMAX_DELAY);
    
    TickType_t currentTime = xTaskGetTickCount();
    
    // Debounce: check if enough time has passed since last capture
    if ((currentTime - lastCaptureTime) < debounceDelay)
    {
      printk("Camera trigger ignored (debounce)\n");
      continue;  // Ignore this trigger
    }
    
    lastCaptureTime = currentTime;
    
    // Send capture command to PC via UART
    __HAL_UART_DISABLE_IT(&huart2, UART_IT_RXNE);
    HAL_StatusTypeDef status = HAL_UART_Transmit(&huart2, (uint8_t *)captureCommand, strlen(captureCommand), 100);
    __HAL_UART_ENABLE_IT(&huart2, UART_IT_RXNE);
    
    if (status == HAL_OK)
    {
      printk("Camera capture command sent\n");
    }
    else
    {
      printk("Camera capture command FAILED: %d\n", status);
    }
  }
}

